int		ft_atoi(char *str)
{
	int	sign;
	int	sum;

	sign = 1;
	sum = 0;
	while ((*str >= '\t' && *str <= '\r') || *str == ' ')
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9' && *str != '\0')
	{
		sum = 10 * sum + (*(str) - '0');
		str++;
	}
	return (sum * sign);
}
