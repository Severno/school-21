int		atoi(char *s)
{
	int i;
	int n;
	int sign;

	n = 0;
	i = 0;
	sign = 1;
	if (s[0] == '-')
	{
		sign = -1;
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		n = 10 * n + (s[i] - '0');
		++i;
	}
	return (n * sign);
}
