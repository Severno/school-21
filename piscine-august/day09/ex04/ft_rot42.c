char	*ft_rot42(char *str)
{
	int n;

	n = 0;
	while (str[n])
	{
		if ((str[n] >= 'a'
		&& str[n] <= 'j')
		|| (str[n] >= 'A'
		&& str[n] <= 'J'))
			str[n] = str[n] + 16;
		else if ((str[n] >= 'k'
		&& str[n] <= 'z')
		|| (str[n] >= 'K'
		&& str[n] <= 'Z'))
			str[n] = str[n] - 10;
		str[n] = str[n];
		n++;
	}
	return (str);
}
