int		ft_sqrt(int nb)
{
	int i;
	int sqrt;

	i = 0;
	sqrt = i * i;
	while (i <= nb / 2)
	{
		if (sqrt == nb)
			return (i);
		else if (sqrt > nb)
			return (0);
		i++;
		sqrt = i * i;
	}
	return (0);
}
